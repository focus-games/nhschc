							<h1 style="color: #095EB1;">When should a checklist be completed?</h1>
							
						<p>Screening and assessment of eligibility for NHS Continuing Healthcare (CHC) should be at the right time and location for the individual, and when their ongoing needs are known. Screening is required when there may be a need for CHC. Local health and social care joint processes should be in place to identify individuals for whom it may be appropriate to complete a Checklist, including for individuals in community settings. Wherever an individual requires a long-term care home placement with nursing or has significant support needs, a Checklist would be expected to be completed (unless the decision is made to go straight to the completion of a Decision Support Tool).</p>
								
						<p>Before the Checklist is applied, practitioners should ensure that the individual and (where appropriate) their representative, understands that the Checklist is only an indicator of the need for a full assessment, and not an indicator of eligibility.
The Checklist threshold has been set intentionally low; the significant majority of people who have a positive Checklist and are then fully assessed for CHC will be found not to be eligible. Please refer to paragraphs 85, 87 and 95 in the National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (Revised 2018).
</p>
						
								
						<p>It is important for practitioners to explain that:
							<ul>
								<li>If the individual does not ‘screen in’ as requiring a full assessment for CHC, or if they do screen in but are then found not to be eligible for CHC, it does not mean that they won't get any help – it just means that it will not be through CHC.</li>
								<li>If the individual ‘screens in’ using the Checklist then their needs will be considered in more detail using the Decision Support Tool (DST). This will be done by at least two professionals – referred to as the ‘multi-disciplinary team’ or MDT.</li>
								<li>Only those assessed and found to have a 'primary health need' are eligible for CHC – the term 'primary health need' has a very specific meaning.</li>
								<li>Whether someone has a primary health need depends on the type and level of their day to day nursing/healthcare needs. It does not depend on having a particular diagnosis or disability. The vast majority of people who are ill or disabled do not have a primary health need.</li>
								<li>The individual / representative will be fully involved in the process, so long as they wish to be, and their views will be heard and questions answered.</li>
								<li>The process takes time and will consider the individuals’ current and ongoing needs in depth.</li>
							</ul>
						</p>
							
								<p><b style="color: #000">Practitioners need to be sensitive to the fact that for some people the CHC assessment process can be upsetting.</b></p>
										
