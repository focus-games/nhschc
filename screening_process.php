							<h2>Gaining consent for screening</h2>
							<p>The <!-- <a href="docs/consent_form.pdf"> --> consent form<!-- </a> --> clarifies section 72 of the National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018) and is also compliant with GDPR Regulations. </p>
							
							<p>Consent is not required to process personal data for NHS CHC purposes – this can be carried out using the public task/official authority and provision of healthcare processing conditions. Using consent as the basis for processing personal data will mean that you give the patient the right to withdraw that consent at any time and in any form (verbal or written). If they do this, you cannot switch to a different processing condition to carry on providing NHS CHC assessment and care, and circumvent the wishes of the patient.</p>
							
							<p>However, you will still need to obtain consent to satisfy the Common Law Duty of Confidence and Medico-Legal reasons, and it is important to be clear that you are obtaining consent for Confidentiality and Medico-Legal reasons, not processing. You will still need to inform the patient about the use of their information, so they can give their consent based on what they expect to happen.</p>
							
								<p><b>There is a <!-- <a href="docs/consent_form.pdf" target="_blank"> --> consent template form <!-- </a> --> available within the NHS CHC delivery model. If you are not using the consent template, you should ensure that one you are using is GDPR compliant. </b></p>
							
