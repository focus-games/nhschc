							<h1 style="color: #095EB1;">What is a Primary Health Need?</h1>
							
							<p>To assist in determining which health services it is appropriate for the NHS to provide under the NHS Act, and to distinguish between those and the services that local authorities may provide under the Care Act 2014, the concept of a ‘primary health need’ was developed. Where a person has been assessed to have a primary health need, they are eligible for NHS Continuing Healthcare and the NHS will be responsible for providing for all the individual’s assessed health and associated social care needs, including accommodation, if that is part of the overall need. Determining whether an individual has a primary health need involves looking at the totality of the relevant needs.</p>
							
							<p>To determine whether an individual has a primary health need, an assessment of eligibility process must be undertaken by a multidisciplinary team (MDT) which must use the national Decision Support Tool (DST)</p>
							
							<p>An individual has a primary health need if, having taken account of all their needs (following completion of the Decision Support Tool), it can be said that the main aspects or majority part of the care they require is focused on addressing and/or preventing health needs.</p>
							
							<p>Having a primary health need is not about the reason why an individual requires care or support, nor is it based on their diagnosis; it is about the level and type of their overall actual day-to-day care needs taken in their totality.</p>
							
							<p>Where an individual has a primary health need and is therefore eligible for NHS Continuing Healthcare, the NHS is responsible for providing for all the individual’s assessed health and associated social care needs, including accommodation (not including own home – only nursing or care homes), if that is part of the overall need.</p>
							
							<p>Each individual case must be considered on its own needs in accordance with the principles outlined in <a target="_blank" href="https://assets.publishing.service.gov.uk/government/uploads/system/uploads/attachment_data/file/690426/National_Framework_for_CHC_and_FNC_-_October_2018_Revised.pdf">this National Framework</a>, pages 19-21.</p>
						
