							<h1 style="color: #095EB1;">Local Resolution</h1>
					<p><em>Where an individual or their representative asks the CCG to review the NHS Continuing Healthcare (CHC) eligibility decision, this should be addressed through the local resolution procedure, which is normally expected to resolve the matter. CCGs should deal with requests for review in a timely manner.</em> (Paragraph 194 National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018)).</p>
					
					<p>The National Framework 2018 paragraph 195 sets out the requirement that all CCGs must have a published CHC local resolution process that is fair, transparent, includes timescales and takes account of the following guidelines:</p>
					
					<p><em>There should be an attempt to resolve any concerns initially through an informal two-way meaningful discussion between the CCG representative and the individual and/or their representative. There should be a written summary of this for both parties. The discussion should be an opportunity for the individual or their representative to receive clarification of anything they have not understood. The CCG should explain how it has arrived at the decision regarding eligibility, including reference to the completed DST and primary health need assessment. Where required this should also be an opportunity for the individual or their representative to provide any further information that had not been considered. </em></p>
					
					<p><em>Where a formal meeting involving the individual and/or their representative is required, this should involve someone with the authority to decide next steps on behalf of the CCG (e.g. to request further reports, or seek further clarification/reconsideration by the MDT). The individual should be able to put forward the reasons why they remain dissatisfied with the CCGâ€™s decision. National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care 56 There should be a full written record of the formal meeting for both parties. The CCG will agree next steps with the individual or their representative.</em></p>
					
					<p><em>Following the formal meeting and outcome of the next steps, the CCG will either uphold or change the original eligibility decision. </em></p>
					
					<p><em>A key principle of the local resolution process is that, as far as possible, if the CCG does not change the original decision, the individual or their representative has had a clear and comprehensive explanation of the rationale for the CCG decision. </em></p>
					
					<p><em>Where individuals wish to move straight to a formal meeting this should be considered. CCGs should use every opportunity to learn from these meetings, and should consider how they share their learning with other CCGs. </em></p>
					
					<p><em>CCGs may choose to prioritise cases for individuals currently in receipt of care.</em></p>
						
						
