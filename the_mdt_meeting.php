							<h1 style="color: #095EB1;">Agreed Joint Health and Social Care Statement: Participation in the Multidisciplinary Team Meeting (MDT)</h1>
							
						
						<p>The MDT should ensure that NHS Continuing Healthcare (CHC) assessment is person-centred, keeping the individual/their representative at the heart of the process throughout. It is vital that the individual’s experience of the process is as good as it can be, minimising the potential for dissatisfaction and complaint.</p>

<p>The MDT process is designed to be collaborative in order to fully consider and accurately reflect the individual’s needs in the Decision Support Tool (DST). Assessing eligibility for CHC is not an adversarial process and MDT members should act accordingly.</p>

<p>There is no hierarchy in the MDT. All members should work collaboratively and share responsibility for the recommendation, recognising the different skills and knowledge of those involved.</p> 

								<p>No member of the MDT is individually responsible for the recommendation of the MDT.</p>

<p>The MDT recommendation must be based on a full consideration of the individual’s needs as evidenced by the written and verbal information provided, particularly from those who have direct knowledge of the individual. Sometimes it will be necessary to obtain additional evidence to ensure a full picture of the needs.</p>

<p>The MDT is required to make a recommendation and must apply the Primary Health Need test as set out in the National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018) paragraphs 54 – 66.</p>

<p>Professional disagreements may well occur and can be very constructive but should be resolved in a spirit of collaboration. Where there continues to be disagreement this should be recorded in the DST.</p>

<p>The individual/their representative should not be drawn into a disagreement between professionals.</p>

<p>Once a recommendation has been agreed by an MDT, no organisation or manager should change or dispute that recommendation afterwards, unless this forms part of an agreed local dispute resolution procedure between the local authority and the CCG, or constitutes a Framework-compliant application of the National Framework requirements regarding CCG verification of eligibility recommendations.</p> 

