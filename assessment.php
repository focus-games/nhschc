							<h1 style="color: #095EB1;">Characteristics of a good quality MDT Assessment</h1>
							
							<p>Process of gathering relevant, accurate and up to date information about an individual`s health and social care needs.</p>
							<p>As a minimum a good quality MDT assessment of an individual`s health and social care needs will be:
							<ul>
												<li>Preceded by informed consent or appropriate best interest’s decision</li>
												<li>Proportionate to the situation</li>
												<li>Person centred</li>
													<li>Informed by those directly caring for the individual</li>
														<li>Holistic. Looking at the range of needs from different professional and individual view points</li>
															<li>Taking into account differing professional views and reaching a common agreement</li>
																<li>Considerate of the impact of the individual’s needs on others</li>
																<li>Focused on improved outcomes for the individual</li>
																<li>Evidenced based</li>
																<li>Clear about needs requiring support</li>
																<li>Clear regarding the degree and nature of any risks to the individual (or others), the individuals view on these, and how best to manage the risks</li>
