							<h1 style="color: #095EB1;">Principles of consent</h1>
							
						
						<p>The Checklist is the NHS Continuing Healthcare (CHC) screening tool which can be used in a variety of settings to help practitioners identify individuals who may need a full assessment of eligibility for NHS Continuing Healthcare. It is essential that the appropriate consent is sought prior to commencing this process, not for GDPR (EU General Data Protection Regulation) but for the requirements of the common law duty of confidentiality and for medico-legal reasons. </p>	
						
						<p>Following the implementation of GDPR through the Data Protection Act 2018, consent is not required for the processing of someone’s health information for the purposes of providing health or social care. However, the policy position is that consent is still required for other reasons, not least because people other than health and social care professionals may be involved in the CHC assessment process and because a core principle is transparency and involvement of the individual/their representative.</p>
						
							<h2>Principles of consent</h2>	
							<p>Where the individual has capacity, their consent to share confidential information outside those professionals involved in the assessment should be obtained <b style="color: #000">before</b> the start of the process</p>
							
							<h2>For consent to be valid it must be:</h2>
											
											<ul>
												<li>Explicit</li>
												<li>Specific</li>
												<li>Informed</li>
												<li>Freely given</li>
												<li>Able to be withdrawn</li>
										
											</ul>
									
								<p>The CCG has a duty to assess if someone may be eligible for CHC. The assessment information will be held by the CCG for as long as is necessary, depending on the circumstances. GDPR does not require consent for professionals to hold this information. </p>
								
								<p>However, there may be confidential information about the individual shared outside the multi-disciplinary team (MDT) who are carrying out the assessment (e.g. with friends or family of the individual). Consent from the individual to share that confidential information with them should be sought in the usual way; professionals need to be mindful of the common law duty of confidentiality, which still applies, and because the CHC assessment process should be as transparent and explicit as possible. </p>
								
								<p>If an individual with capacity does not consent to their information being shared for CHC, the potential consequences of this should be carefully explained. This might affect the ability of the NHS and the local authority to provide appropriate services to them.</p>
								
								<p>The fact that an individual declines to be assessed for CHC does not, in itself, mean that a local authority has an additional responsibility to meet their needs, over and above the responsibility it would have had if they had been assessed for CHC.</p>
								
								<p>Where there are concerns that an individual may have significant ongoing needs, and that the level of appropriate support could be affected by their decision to withhold consent to sharing confidential information, the appropriate way forward should be considered jointly by the CCG and the local authority, taking account of each organisation’s legal powers and duties. It may be appropriate for the organisations involved to seek legal advice (refer to Practice Guidance note 5 and 6 of the National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018) for more information).</p>
				
