												<li>Needs assessment (under the Care Act 2014)</li>
												<li>Any allied health professional assessments</li>
												<li>Psychiatric/community psychiatric nurse assessments</li>
												<li>Any relevant charts, diaries, risk assessments, reports, clinic letters, emails etc</li>
												<li>Any specialist assessments which are relevant to the individual’s needs (which may need to be specifically requested).</li>
												<li>Individual’s own views of their needs and desired outcomes</li>
