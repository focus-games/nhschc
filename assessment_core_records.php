												<li>Carer’s views</li>
												<li>Physiotherapy assessment</li>
												<li>Behavioural assessment</li>
												<li>Speech and Language Therapy (SALT) assessment</li>
												<li>Occupational Therapy assessment</li>
												<li>Care home/home support records</li>
												<li>Current care plan</li>
												<li>24-hour/48-hour diary indicating needs and interventions (may need to be ‘good day’ and ‘bad day’ if fluctuating needs)</li>
												<li>Specialist medical/nursing assessments (e.g. tissue viability nurse, respiratory nurse, dementia nurse, etc.)</li>
												<li>Falls risk assessment</li>
												<li>Standard scales (such as the Waterlow score)</li>
												
