							<h1 style="color: #095EB1;">Agreed Joint Health and Social Care Statement: Preparing for the Multidisciplinary Team (MDT) Meeting</h1>
							
						
<p>It is the responsibility of the co-ordinator to ensure that all relevant people are invited to participate in the MDT, in the light of the Framework requirements. In particular, where appropriate, those with specialist knowledge regarding the individual’s needs should be included (see National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018) paragraph 126).</p>

<p>The membership of the MDT should comprise professionals who can accurately identify the needs of the individual and, where possible, have recently been involved in the assessment, treatment or care of the individual.</p> 

<p>As a minimum it is expected that the Local Authority (LA) will normally be involved in the MDT process wherever they have a Care Act Assessment or other relevant information regarding the individual’s needs. The National Framework 2018 encourages a joint health and social care approach to CHC assessments wherever this is practicable, particularly where the local authority has made the referral for a CHC assessment.</p>

<p>If the LA is not able to commit to attending all CHC MDTs, they should agree protocols with the CCG identifying the circumstances when they will not directly participate in the MDT process. These protocols should clarify how the CCG will consult with the LA over eligibility decisions in such circumstances. These protocols should also include how work will be prioritised if there are issues around workforce capacity, bearing in mind the minimum requirements for an MDT as set out in the National Framework 2018.</p>

<p>If it is not possible for the LA to arrange for a practitioner to attend the MDT meeting within a reasonable timeframe, consideration should be given to the use of technology (e.g. teleconference or video link) in order for them to be involved in the MDT process (with reference paragraph 26.2 Practice Guidance of the National Framework 2018). Similar arrangements could be considered for other members of the MDT.</p>

<p>The CCG should be flexible and give as much notice as possible to the LA when requesting ‘advice and assistance’, including assessment reports, or requesting participation in MDTs. It is expected that agreed timescales should form part of local protocols.</p>

<p>A prompt response from the LA is important in order to help ensure that most eligibility decisions can be made within a 28 day timescale (see National Framework 2018 paragraph162 and Practice Guidance 20.1 and 28.2). </p> 

<p>The LA should establish systems to enable participation in MDTs in a timely manner. For example, through a pre-planned schedule of availability of staff. </p> 

<p>When convening an MDT, consideration should be given as to whether the individual requires, or is choosing to involve, an advocate. </p> 
					
<p>Also, when convening an MDT, consideration should be given to any possible requirements for reasonable adjustments to facilitate participation in the assessment process.</p>
						
						
							
						
