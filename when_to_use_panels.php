							<h1 style="color: #095EB1;">When to use Panels </h1>
						
							<p>There is no requirement for CCGs to use a panel and they should not replace the function of the Multidisciplinary Team (MDT). Decision-making responsibility for NHS Continuing Healthcare (CHC) rests with the CCG, bearing in mind that only in exceptional circumstances, and for clearly articulated reasons, should the MDT recommendation not be followed. </p>
							
								<p>There is a duty on the CCG to consult with the local authority (LA), wherever practicable, before making an eligibility decision. Normally this consultation will be through the eligibility assessment process, but where the local authority has not been involved in the MDT then a different mechanism for consulting the LA is needed. Some CCGs choose to do this through a panel arrangement, but other mechanisms may be more efficient.</p>
								
								<p>CCGs are not encouraged to use panel arrangements, but if they do, this should only be on a selective basis in order to support consistent decision-making, for example:
							
							<ul>
												<li><em>Cases which are not recommended as eligible for NHS Continuing Healthcare (for audit purposes or for consideration of possible joint funding).</em></li>
												<li><em>Cases where there is a disagreement between the CCG and the local authority over the recommendation.</em></li>
												<li><em>Cases where the individual or his/her representative is appealing against the eligibility decision.</em></li>
												<li><em>A sample of cases where eligibility has been recommended for auditing/ learning processes to improve practice (National Framework 2018 â€“ Practice Guidance paragraph 38.2)</em></li>
														
											</ul></p>
											
