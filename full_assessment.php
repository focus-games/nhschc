					function checkCookie() {
					    var username = getCookie("full_assessment");
					    if (username != "") {
					        //get cookie
					        $page_name = getCookie("full_assessment");
					        openSection(event, $page_name);
					    } else {
					        //no cookie
					        openSection(event, 'who_is_involved');
					    }
