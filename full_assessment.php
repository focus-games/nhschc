						<p> The multidisciplinary team (MDT) will use a Decision Support Tool (DST) to determine whether an individual has a primary health need by looking at all of an individual's care needs in their totality, relating them to:</p>
<ul style="margin-bottom: 1em;">
						<li>what care an individual needs</li>
						<li>how complex their care needs are</li>
						<li>how intense or severe their care needs can be</li>
<li>how unpredictable their condition is, including any risks to an individual's health if the right care isn't provided at the right time</li>
</ul>

<p>An individual's eligibility for NHS Continuing Healthcare depends on their assessed care needs and whether practitioners have identified that they have a primary health need. Eligibility is not assessed on any particular diagnosis or condition.</p>
							
