							<h2 style="font-size: 1.4em;">The day of the MDT meeting</h2>
						
							<p>The <b style="color: #095EB1;">multidisciplinary team's</b> assessment, using the <b style="color: #095EB1;">Decision Support Tool</b>, will consider the individual's care needs under these headings: 
							
							<div class="row 50%">
								
										<div class="6u 12u(narrower)">
										
												<ul>
								
								<li>Breathing</li>
								<li>Nutrition - Food and Drink</li>
								<li>Continence</li>
								<li>Skin</li>
								<li>Mobility</li>
								<li>Communication</li>
								<li>Psychological and Emotional Needs</li>

								
							</ul>
										
										</div>
										
										<div class="6u 12u(narrower)" >
										
												<ul>
								
								
								<li>Psychological and Emotional Needs</li>
								<li>Cognition</li>
								<li>Behaviour</li>
								<li>Drug Therapies and Medication: Symptom Control</li>
								<li>Altered States of Consciousness (ASC)</li>
								<li>Other significant care needs to be taken into consideration</li>
								
							</ul>
										
										</div>
								
							</div>
							
							
These needs are given a weighting marked "priority", "severe", "high", "moderate", "low" or "no needs".
<br><br>
If the individual has at least one priority need, or severe needs in at least two areas, the individual should be eligible for NHS Continuing Healthcare. The individual may also be eligible if they have a severe need in one area plus a number of other needs, or a number of high or moderate needs, depending on their nature, intensity, complexity or unpredictability. 
<br><br>
In all cases, the overall need, and interactions between needs, will be taken into account, together with evidence from risk assessments, in deciding whether NHS Continuing Healthcare should be provided.</p>
