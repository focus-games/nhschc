																<h2>Monitoring and review</h2>
																
							
					<p>Personal Health Budgets and personalised care and support plans should be reviewed initially within the first three months and then on an annual basis. However, reviews may also take place at other times, for example, where there is a change in clinical needs, or at the request of the person. People should have upfront information about the frequency of reviews and what will be expected during the process.</p>
     												
      												<p>At all times there should be a clear separation between review of the personalised care and support plan and budget, audit of the direct payment, and eligibility assessment for NHS Continuing Healthcare.</p>
      												
      												<p>Regardless of how budgets are set, it is important to remember that people need to know upfront how much money they have to spend on their care and support and, how this was calculated to enable them to effectively plan the care and support they need.</p>
      												
      												<p>Whilst CCGs should endeavor to co-ordinate reviews for NHS Continuing Healthcare eligibility and Personal Health Budgets within a similar timeframe, they should be mindful of not over-burdening people and families e.g. not to schedule these for the same day.</p>
      												
      												<p>A review should address:</p>
      												<ul>
      												<li>How well the personalised care and support plan is meeting the person’s health needs and their agreed outcomes, and identifying any new outcomes.</li>
      												<li>Whether health needs have changed, and if so whether the personalised care and support plan needs updating.</li>
      												<li>Whether the Personal Health Budget has been used effectively and appropriately.</li>
      												<li>Whether the risks have changed and whether the risk management is still effective.</li>
      												<li>Whether the Personal Health Budget is sufficient to cover the full cost of each of the services identified to meet the person’s assessed needs and identified outcomes.</li>
      												</ul>
      												
      												<p>Actions identified during a review should be followed up in a timely way. Any changes to the budget should be discussed in an open and transparent way, and people should be aware of the process to manage any disputes should they not be satisfied with the proposed changes.</p>
      												
      												<p>People should be given opportunities for an informal review to check that they are managing the budget in the way they have been asked to and should know who to contact if they are worried about anything. </p>
      												
      												<p>CCGs can use the review process to identify any market development opportunities or pin point gaps in the availability of services and support that people may wish to purchase with their Personal Health Budget. The review process may also signal the need to review budget setting approaches to ensure budgets offered are financially viable and people have sufficient funds available to purchase care and support based on local market rates.</p>
      												
      												<p>Personal Health Budget reviews should be a supportive process and a positive experience for people, not a means to enforce compliance.</p>
							
				
