																<h2>Making contact and getting clear information</h2>
											
							
						<p>A Personal Health Budget is an amount of money to support a person's identified health and wellbeing needs, planned and agreed between the person and their local NHS team or healthcare professional. It is not new money, but a different way of spending health funding to meet a person's identified needs.</p>
						<p>Personal Health Budgets enable people to have greater choice, flexibility and control over the healthcare and support they receive.</p>
						
						<p>Personal Health Budgets can be managed in one of three ways, or a combination of these:</p>
						
						
						<p><strong>Notional budget:</strong> The NHS holds the money on behalf of the individual. The person knows how much their budget is and discusses with the NHS the care and support they require to meet their needs. The NHS purchases the agreed care and support.</p>
						<p><strong>A third party budget:</strong> An organisation independent of the individual and the NHS holds the budget and makes the arrangements for the agreed care and support. If the budget is used to employ personal assistants, it is the organisation holding the budget who is their registered employer.</p>
						<p><strong>A direct payment:</strong> Money is paid directly into the individual's (or their representative's) bank account and they can directly purchase the care and support required in line with the agreed personalised care and support plan.</p>
							
						
							
				
