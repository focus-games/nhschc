							<h2 style="font-size: 1.4em;">Working out the amount of money available</h2>
						
							<p>An indicative budget is the initial calculation of how much money the person is likely to need to meet their assessed care and support needs. NHS care is free at the point of delivery and in NHS Continuing Healthcare the Clinical Commissioning Groups (CCGs) are financially responsible for all of the person’s assessed health and associated social care needs - the budget must be sufficient to meet these assessed needs. 
The assessment of need completed in Step 2 provides information to inform indicative budget setting. 

</p>
						
						<p>CCGs will have their own local processes for setting and agreeing budgets. These will be determined by a number of different things, for example, the way in which services are already commissioned and the money already available to meet people's care and support needs.</p>
						
						<p>Regardless of how budgets are set, it is important to remember that people need to know upfront how much money they have to spend on their care and support and, how this was calculated to enable them to effectively plan the care and support they need.</p>
						
						<p>CCGs should review their budget setting models at regular intervals to ensure indicative cost calculations are compliant with any changes in statutory requirements (e.g. changes in national minimum wage, pension contributions, employers national insurance contributions etc.) and financially viable for the local marketplace.</p>
							
							
																	</div>	
																	
																	<div class="process-section" id="a">
													<h3>What this means for people in receipt of NHS Continuing Healthcare</h3>
     										<ul>
      										<li>I know how much money is available within my indicative budget and how it was calculated.</li>
      										<li> I have enough money to help me start to plan for how I will meet my assessed health and social care needs.</li>
											</ul>
							
							
						</div>
																	
																									<div class="process-section" id="a">
																<h3>Acceptable standard</h3>
      												<ul>
      												<li>A sustainable budget setting approach consistent with existing service offer costs - no new money available</li>
      												<li>Budget setting model that ensures the PHB is clear, upfront, transparent and enough to meet assessed needs</li>
      												<li>Fair and equitable offer across all three budget deployment options – notional budget, direct payment, third party budget</li>
      												<li>Clear and timely process for signing off the indicative budget</li>
      												<li>In circumstances where it is needed or requested, a dispute resolution pathway is available to the person</li>
      												</ul>
											
							
