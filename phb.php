							<div class="process-section" id="d">
							<h2 style="font-size: 1.4em;">Developing a personalised care and support plan</h2>
						
							<p>At the centre of a Personal Health Budget is a personalised care and support plan. 
The personalised care and support plan should clearly set out the health needs that the Personal Health Budget is to address and be agreed between the NHS and the person receiving the Personal Health Budget.</p>
      												<p>The personalised care and support plan should set out the outcomes that are intended to be achieved and how the budget will be spent to enable the person to meet these outcomes. It is important for CCGs to be flexible when agreeing budget-spend, as far as possible, to enable people to live as they would like within the context of their health condition.</p>
      												
      												<p>A good personalised care and support planning discussion will identify a person’s strengths, skills and personal circumstances as well as their health needs. It will identify what is working and not working from their perspective, what is important to the person and what is important for their health.</p>
      												
      												<p>Support for personalised care and support planning can take many forms. Support may be from the person’s healthcare professional, but people may prefer an independent person to guide them through the process and liaise with the relevant parties. As with each aspect of a Personal Health Budget, the best approach is to ask the person what would work for them and not assume that the same option suits everyone.</p>
      												
      											<p>It is important to include the support of advocacy where appropriate. This will help ensure that people are able to make informed decisions in their best interests, and that they do not find the process overly burdensome or overwhelming.</p>
      												
      												<p>The personalised care and support plan should be recorded in a format that best suits the person and should be easily accessible for them - whether in paper, digital, pictorial or other format.</p>
      												
      												<p>When agreeing the personalised care and support plan, the CCG needs to be satisfied that:</p>
      												<ul>
      													<li>The person’s health needs can be met through the care and support set out in the plan</li>
      													<li>The amount of money is enough to cover the full cost of the care and support agreed to meet the person’s outcomes</li>
      													<li>The plan can be reviewed as required </li>
      													<li>There is an appropriate risk management plan that's been developed with the person</li>
      													<li>Where people lack capacity or are more vulnerable, procedures such as safeguarding and promoting liberty have been included appropriately</li>
      												</ul>
      												
      												<p>The individual or their representative must also agree that:</p>
      												<ul>
      													<li>The person’s care needs will be met by the care and support agreed in the plan</li>
      													<li>The amount of money available is sufficient to cover the full cost of the plan</li>
      													<li>The plan will be reviewed and their needs may be re-assessed as part of that review (as set out in Step 2)</li>
      													
      												</ul>
      										
								</div>
								
								<div class="process-section" id="a">
													<h3>What this means for people in receipt of NHS Continuing Healthcare</h3>
     										<ul>
      										<li>I am able to identify my outcomes and develop my personalised care and support plan with my care coordinator and any other people I wish to include. </li>
      										<li> I understand what each section of my plan is all about and what information is needed for sign-off.</li>
      										<li>I understand my role in making my plan happen.</li>
											</ul>
							
							
						</div>
								
								<div class="process-section" id="a">
																<h3>Acceptable standard</h3>
      												<ul>
      												<li>A single, named care coordinator for each person is in place</li>
      												<li>Where the person is unable to contribute to the development of their personalised care and support plan, the people who know them best are involved</li>
