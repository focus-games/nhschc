								<h2 style="font-size: 1.4em;">Agreed Joint Health and Social Care Statement: Leadership Values and Behaviours</h2>
																
						
							
							
						<p>The box below contains joint statements that have been agreed between NHS England & ADASS</p>
							
							<p>Successful partnerships are built on strong value based leadership, professional accountability, trust between practitioners and managers across agencies, transparent processes and decision-making, and a culture of professionalism. This is achieved by a combination of ‘individual agency’ and ‘structure’, i.e. the actions of individual practitioners within the context of the rules and culture which their organisation creates.</p>

<p>All parties/agencies involved recognise that NHS CHC is a whole Health and Social Care system issue and that the stronger the partnership the better it is for the system, making best use of health and social care resources and for members of the public that the system serves.</p>

<p>System leaders must create a culture that ensures the process is person centred such that individuals are never be made to feel that they are embroiled in a disagreement between agencies.</p> 

<p>System leaders need to be willing to ask the challenging questions of themselves about their NHS CHC service and partnership arrangements, recognising that care, compassion, competence, communication, courage and commitment are all essential elements and that front-line staff have a right to expect support from system leaders.</p> 

<p>All practitioners should recognise their own individual and organisational responsibilities for their part in the successful operation of a whole system approach including the effective assessment for, and delivery of, NHS CHC.</p>

<p>Organisational processes and individual behaviours regarding NHS CHC should be in accordance with both the word and the spirit of the Framework, based on a fair, transparent and person-centred approach.</p>

<p>It is essential that practitioners approach the assessment of eligibility in a spirit of fairness and transparency and without organisational bias. In this way all, and only, those individuals who have a Primary Health Need should be identified as eligible for NHS CHC. The experience of the individual being assessed, and of any representative/family member involved, should be as positive as possible irrespective of the eligibility outcome.</p>
							
