							<h1 style="color: #095EB1;">Agreed Joint Health and Social Care Statement: Proportionate Screening Using the Checklist</h1>
							
						
						
<p>CCGs, along with their local authority (LA) partners, should agree pro-active systems to ensure that screening arrangements are effective across all settings and for <b style="color: #000">all</b> adult client groups. They should have in place agreed protocols that include arrangements for proportionate and timely use of the Checklist in accordance with the National Framework 2018 requirements. This should include when it is not necessary or appropriate to screen for NHS Continuing Healthcare.</p>

<p>LAs and CCGs should work together to ensure that the decision whether or not to Checklist, and the completion of Checklists, are undertaken by practitioners with the relevant skills and training. </p> 

<p>The decision to undertake CHC screening should be based on the needs and situation of the individual, rather than on administrative or organisational requirements (e.g. screening purely because the individual’s case is being considered by an LA funding panel).</p> 

<p>Health and Social Care agencies are responsible for ensuring that any Checklists completed by their staff are of good quality and accurately reflect the individual’s needs. </p> 

<p>CCGs should not impose disproportionate quality assurance mechanisms for Checklists which result in a barrier to, or delay in, individuals being referred for CHC assessment. Positive Checklists should be acted upon as soon as possible unless critical information is missing, in which case the missing information should be obtained without delay.</p> 

<p>Where poor practice regarding screening is identified in a team/environment then targeted intervention (for example bespoke training) should be provided in order to address the issues.</p>

<p>CCGs and system partners should agree an escalation process if cases are being Checklisted inappropriately or are being denied a Checklist inappropriately. This could form part of the locally agreed dispute resolution process/policy.</p> 

<p>Arrangements must be in place to inform individuals and/or their representatives of the outcome of the Checklist (whether positive or negative) in writing. There should be agreement between health and social care agencies as to how this requirement will be met, bearing in mind that Checklists are completed by a wide range of practitioners across different organisations.</p> 

<p>Health and social care agencies should agree arrangements to ensure that the CCG receives completed Checklists as soon as possible after completion and also that it keeps a record of the outcome of all Checklists completed, both positive and negative.</p> 
						
