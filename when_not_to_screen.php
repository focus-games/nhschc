							<div class="process-section" id="a">
							<h1 style="color: #095EB1;">When is it not necessary to complete a checklist?</h1>
							
						
							<p style="margin-bottom: 0.5em"><b style="color: #000">The National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018) paragraph 91, makes it clear that there are many situations where it is not necessary to complete a CHC Checklist, including:</b></p>
      												
      												
      												<p>
      												<ul>
      												<em><li>When it is clear to practitioners working in the health and care system that there is <b style="color: #000">no need for NHS Continuing Healthcare at this point in time.</b> Where appropriate/relevant this decision and its reasons should be recorded. If there is doubt between practitioners a Checklist should be undertaken. </li>
     												<br>
      												<li>The individual has <b style="color: #000">short-term health care needs or is recovering from a temporary condition</b> and has <b style="color: #000">not yet reached their optimum potential</b> (if there is doubt between practitioners about the short-term nature of the needs it may be necessary to complete a Checklist).  Please also read ‘Understanding how hospital discharge interacts with CHC’. </li>
      												<br>
      												<li>When the individual has been admitted to hospital from a package or residential placement, which all relevant parties agree can still safely and appropriately meet their needs without any changes, then the completion of the Checklist, if required, should take place within 6 weeks of the individual returning to the place from which they were admitted to hospital. </li>
      												<br>
      												<li>When it has been agreed by the CCG that the individual should be <b style="color: #000">referred directly for full assessment</b> of eligibility for NHS Continuing Healthcare. 

</li><br>
      												
      												<li>If the individual has a <b style="color: #000">rapidly deteriorating condition</b> and may be entering a terminal phase – in these situations the Fast Track Pathway Tool should be used instead of the Checklist. </li>
      												
      												<br>
      												<li>An individual is <b style="color: #000">receiving services under Section 117</b> of the Mental Health Act that are meeting all of their assessed needs. </li>
      													
      													<br>
      												<li><b style="color: #000">It has previously been decided</b> that the individual is not eligible for NHS Continuing Healthcare and it is clear that there has been no change in needs.</li>
      												<br>
      												<li>When a checklist has already been completed and the individual screened out. I.e. FNC review and the individual’s needs have not changed since the last review/checklist.</li></em>
      												
      												
      												</ul>
      												If it is deemed not necessary to complete a checklist at this time, it should be recorded in the individual’s notes, including the reason/s why, and dated and signed.
      												</p>	
						
