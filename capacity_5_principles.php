							<h1 style="color: #095EB1;">Capacity – Five principles</h1>
							
						
		<p><em>If there is a concern that the individual may not have capacity to give consent to the assessment process or the sharing of information, this should be determined in accordance with the Mental Capacity Act 2005 and the associated code of practice.</em></p>
					
							
				
						
													<p style="margin-bottom: 0.5em"><b style="color: #000">Five principles of the Act: </b></p>
							
							<ul>
												<li><em>A person must be assumed to have capacity unless it is established that they lack capacity. </em></li>
								
												<li><em>A person is not to be treated as unable to make a decision unless all practicable steps to help them to do so have been taken without success. </em></li>
								
								<li><em>A person is not to be treated as unable to make a decision merely because they make an unwise decision. </em></li>
								

								<li><em>An act done, or decision made, under this Act for or on behalf of a person who lacks capacity must be done, or made, in their best interests. </em></li>
								
								<li><em>Before the act is done, or the decision is made, regard must be had to whether the purpose for which it is needed can be as effectively achieved in a way that is less restrictive of the person’s rights and freedom of action. </em>
(Paragraph 74 National Framework for NHS Continuing Healthcare and NHS-funded Nursing Care (revised 2018)).
</li>
												
											</ul>
											
								<p><em>It is important to be aware that just because an individual may have difficulty in expressing their views or understanding some information, this does not in itself mean that they lack capacity to make the decision in question.  Appropriate support and adjustments, for example, using alternative methods of communication, should be made available to the person in compliance with the Mental Capacity Act 2005, and with disability discrimination legislation.</em> (Paragraph 75 National Framework 2018)</p>	
								
								<p>Capacity is both time specific and decision specific.</p>
								

									
      												<p style="margin-bottom: 0.5em"><b style="color: #000">Role of CCG & Local Authority </b></p>
      												<p>CCGs and local authorities should ensure that all staff involved in NHS Continuing Healthcare assessments are appropriately trained in Mental Capacity Act 2005 principles and responsibilities. (Paragraph 75 National Framework 2018)</p>
      												
