							<h1 style="color: #095EB1;">Information required for the MDT meeting </h1>
							
						
						<p>Assessment process is of gathering relevant, accurate and up-to-date information about an individual’s health and social care needs and applying professional judgement to decide what this information signifies in relation to those needs. Both information and judgement are required. Simply gathering information will not provide the rationale for any eligibility recommendation; a recommendation that simply provides a judgement without the necessary information will not provide the evidence for any subsequent decision. Assessment documentation should be obtained from any professional involved in the individual’s care and should be clear, well-recorded, factually accurate, up to date, signed and dated.</p>
						<p>Good quality assessment material should include:
							
							<ul>
												<li>Person centred information relating to the individuals views and aspirations </li>
												<li>Information which is relevant and proportionate with sufficient depth</li>
												<li>Information from those providing care</li>
												<li>Holistic review of the needs from both professionals and personal viewpoints</li>
												<li>Professional views discussed to reach a commonly agreed conclusion where possible</li>
												<li>Impact of the individual’s needs on others</li>
												<li>Outcome focused care</li>
												<li>Evidence based </li>
												<li>Care home/care provider support record eg care plan, risk assessments, daily records, behaviour charts, specialist plans, GP/Consultant records, standard monitoring tools (such as Waterlow score) </li>
