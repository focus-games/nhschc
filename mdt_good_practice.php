							<div class="process-section">
							<h1 style="color: #095EB1;">What is an MDT?</h1>
							
							<h2>Purpose:</h2>
							<p>The core purpose of the MDT is to make a recommendation on eligibility for NHS Continuing Healthcare drawing on the multidisciplinary assessment of needs.</p>
							
							</div>
							<div class="process-section">
							
							<h2>Multidisciplinary Team:</h2>
							<p>In accordance with regulations an MDT means a team consisting of at least: </p>
							
							<ul>
												<li>Two professionals who are from different healthcare professions, or </li>
												<li>One professional who is from a healthcare profession <b>and</b> one person who is responsible for assessing persons who may have needs for care and support under part 1 of the Care Act 2014. </li>
												
											</ul>
											
											<p>Whilst as a minimum requirement an MDT can comprise two professionals from different healthcare professions, the MDT should usually include:</p>
							
							<ul>
												<li>Both health and social care professionals, who are knowledgeable about the individual’s health and social care needs</li>
												<li>Where possible, have recently been involved in the assessment, treatment or care of the individual.</li>
												
											</ul>
											
											<p>It is important that those contributing to this process have the relevant skills and knowledge.</p>
											<p>It is best practice that where the individual concerned has, for example a learning disability, or a brain injury, someone with specialist knowledge of this client group is involved in the assessment process. Consideration should be given to the timeliness of the assessment, as it must be completed within 28 days.</p>
											
											</div>
							<div class="process-section">
											
											<h2>The MDT works together to: </h2>
							<ul>
												<li>Collate and review the relevant information on the individual’s health and social care needs.</li>
												<li>Use this information to help define individual needs through the completion of the DST</li>
												<li>Collectively make a professional judgement about eligibility for NHS
Continuing Healthcare using the Primary Health Needs test (PHN), which will be reflected in its recommendation.</li>
												
											</ul>
											<p>This process is known as a multidisciplinary assessment of eligibility for NHS Continuing Healthcare.</p>
											
											</div>
							<div class="process-section">
											
											<h2>CCG responsibilities:</h2>
							<p>Standing Rules require that the CCG:</p>
							<ul>
												<li>As far as is reasonably practicable, must consult with the relevant local authority before making any decision about an individual’s eligibility for NHS Continuing Healthcare</li>
												<li>Cooperate with that local authority in arranging for such persons to participate in an MDT for that purpose.</li>
												
												
											</ul>
											<p>CCGs may use a number of approaches (e.g. face-to-face, video/tele conferencing etc.) to arranging MDT assessments in order to ensure active participation of all members as far as is possible.</p>
											
											</div>
							<div class="process-section">
											
											<h2>Local Authority responsibilities:</h2>
							<p>Where the LA is consulted:</p>
							<ul>
												<li>There is a requirement for the LA to provide advice and assistance to the CCG, as far as is reasonably practicable.</li>
										
											</ul>
											<p>A local authority must, when requested to do so by a CCG:</p>
											<ul>
												<li>Co-operate with the CCG in arranging for persons to participate in an MDT, as long as there is an agreed process to give ample notice to the Local Authority and the process should also address what action needs to take place if the LA is not available. Consideration should be given to the timeliness of the assessment, as it must be completed within 28 days.</li>
										
											</ul>
											
											<p>The involvement of local authority colleagues, as well as health professionals in the assessment process, should facilitate the process of ongoing care planning and will make decision-making effective and consistent.</p>
											
											<p>As with any assessments that are carried out, organisations are expected to fair and transparent and focus on individuals’ needs and not their circumstances.</p>
						
